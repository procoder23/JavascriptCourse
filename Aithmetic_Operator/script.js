// Basic arithmetic operators
let a = 5;
let b = 10;

//Addition
const result1 = a + b;
console.log(result1);

//Substraction
const result2 = a - b;
console.log(result2);

//Multiplication
const result3 = a * b;
console.log(result3);

//Division
const result4 = a / b;
console.log(result4);

//Modulous finr reminder
const result5 = a % b;
console.log(result5);

//Increament and decreament
let num = 10;
console.log(num++);
console.log(num--);

// Compound assignment with arithmetic operator
let count = 22;
count += 1;
console.log(count);

count *= 2;
